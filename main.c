void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static uint32_t last_enter = 0;

	if (HAL_GetTick() - last_enter < 200)
		return;

	last_enter = HAL_GetTick();

	if(state == 0)
	{
		if(GPIO_Pin == GPIO_PIN_0)
		{
			clear_cursor();
			main_option = (main_option == 1) ? 3 : main_option - 1;

		}
		if(GPIO_Pin == GPIO_PIN_1)
		{
			clear();
			state = main_option;

		}
		if(GPIO_Pin == GPIO_PIN_2)
		{
			clear_cursor();
			main_option = (main_option == 3) ? 1 : main_option + 1;
		}
	}

}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{

	if (htim->Instance == TIM3)
	{
		monitor_LCD();
	    switch_state();
	}
	if (htim->Instance == TIM2)
	{
		time_cnt += 1;

		if (active_buzzer)
		{
			PWM_Change_Tone(440, volume);
		}

		if(time_cnt == 1000)
		{
			time_cnt = 0;
		}

		HAL_ADC_Start_IT(&hadc2);
	}
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	if (hadc->Instance == ADC2) // LDR
	{
		double LDR_deg = HAL_ADC_GetValue(&hadc2);


		LDR_buffer[LDR_buffer_cnt] = LDR_deg;
		LDR_buffer_cnt++;

		if(LDR_buffer_cnt == 10)
		{
			double sum_LDR = 0;
			for(int i = 0; i < 10; i++)
			{
				sum_LDR += LDR_buffer[i];
			}
			LDR_deg = sum_LDR / 10.0;
			LDR_buffer_cnt = 0;

			LDR_degree = (int) 200 * ((double)LDR_deg / 1023.0);

			if(LDR_degree >= threshold)
			{
				active_buzzer = true;
			}
			else if(LDR_degree < threshold && active_buzzer)
			{
				active_buzzer = false;
				PWM_Change_Tone(0, 0);

			}

		}

	}
}

void PWM_Start()
{
  HAL_TIM_PWM_Start(pwm_timer, pwm_channel);
}

void PWM_Change_Tone(uint16_t pwm_freq, uint16_t volume) // pwm_freq (1 - 20000), volume (0 - 1000)
{

  if (pwm_freq == 0 || pwm_freq > 20000)
  {
    __HAL_TIM_SET_COMPARE(pwm_timer, pwm_channel, 0);
  }
  else
  {
    const uint32_t internal_clock_freq = HAL_RCC_GetSysClockFreq();
	const uint16_t prescaler = 1 + internal_clock_freq / pwm_freq / 60000;
    const uint32_t timer_clock = internal_clock_freq / prescaler;
    const uint32_t period_cycles = timer_clock / pwm_freq;
    const uint32_t pulse_width = volume * period_cycles / 1000 / 2;

    pwm_timer->Instance->PSC = prescaler - 1;
    pwm_timer->Instance->ARR = period_cycles - 1;
    pwm_timer->Instance->EGR = TIM_EGR_UG;
    __HAL_TIM_SET_COMPARE(pwm_timer, pwm_channel, pulse_width); // pwm_timer->Instance->CCR2 = pulse_width;
  }

}

bool check_uart_thresh()
{
	if(uart_buffer[0] == 'l' && uart_buffer[1] == 'i' && uart_buffer[2] == 'g' && uart_buffer[3] == 'h' && uart_buffer[4] == 't' && uart_buffer[5] == ':')
	{
		return true;
	}

	return false;
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	uart_buffer[uart_cnt] = ch;
	uart_cnt++;

	if(ch == ' ')
	{
		HAL_UART_Transmit(&huart4, "\n", 1, HAL_MAX_DELAY);

		if(isMsg)
		{
			memmove(uart_message, uart_buffer, strlen(uart_buffer) + 1);
			uart_cnt = 0;
			isMsg = false;
		}
		else if(check_uart_thresh())
		{
			switch(uart_cnt)
			{
			case 8:
				threshold = uart_buffer[6] - 48;
				break;
			case 9:
				threshold = (uart_buffer[6] - 48) * 10 + (uart_buffer[7] - 48);
				break;
			case 10:
				threshold = (uart_buffer[6] - 48) * 100 + (uart_buffer[7] - 48) * 10 + (uart_buffer[8] - 48);
				break;
			}
			uart_cnt = 0;

		}
	}


	HAL_UART_Receive_IT(&huart4, &ch, 1);

}

void clear_cursor()
{
	setCursor(0,0);
	print(" ");
	setCursor(0,1);
	print(" ");
	setCursor(0,2);
	print(" ");
}

void monitor_LCD()
{
	if(state == 0) //main page
	{
		char light[30];
		sprintf(light, "1-Light");
		setCursor(2,0);
		print(light);

		char msg[30];
		sprintf(msg, "2-Message");
		setCursor(2,1);
		print(msg);

		char settings[30];
		sprintf(settings, "3-Settings");
		setCursor(2,2);
		print(settings);


		switch(main_option)
		{
		case 1:
			setCursor(0,0);
			break;
		case 2:
			setCursor(0,1);
			break;
		case 3:
			setCursor(0,2);
			break;
		}

		print(">");
	}
	else if(state == 1)
	{
		if(time_cnt < 500)
		{
			setCursor(0,0);
			write(2);
		}

		int _light = (int) (((double) LDR_degree / 200.0) * 100);
		char light[30];
		sprintf(light, " Light : %d%% ", _light);
		setCursor(1,0);
		print(light);
		if(time_cnt >= 500)
		{
			setCursor(0,0);
			write(3);
		}

		int _threshold = (int) (((double) threshold / 200.0) * 100);
		char critical[50];
		sprintf(critical, "Critical Light > %d%%", _threshold);
		setCursor(0,2);
		print(critical);

	}
	else if(state == 2)
	{
		char message[50];
		if(time_cnt < 500)
		{
			setCursor(0,0);
			write(0);
			setCursor(1,0);
			write(1);
		}
		sprintf(message, " Message : %s", uart_message);
		setCursor(2,0);
	    print(message);
	    if(time_cnt >= 500)
	    {
	    	setCursor(0,0);
			write(4);
			setCursor(1,0);
			write(5);
	    }
	}
	else if(state == 3)
	{
		if(setThresh)
		{
			HAL_UART_Transmit(&huart4, "Set the threshold : ",20 , HAL_MAX_DELAY);
			setThresh = false;
		}


		char setting[50];
		sprintf(setting, " Threshold : %d", threshold);
		setCursor(0,0);
	    print(setting);

	}
}

void switch_state()
{
	if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0))
	{
		clear();
		state = (state == 3) ? 1 : state + 1;
	}
}

